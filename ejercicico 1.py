#Calcula velocidad promedio, dado e tiempo y distancia

dist = int(input("Teclea la distancia en kms enteros: "))
temp = int(input("Teclea el tiempo de tu viaje en hrs. enteras: "))

velocidad = dist / temp
print("Velocidad promedio: ", velocidad, "km/h")
